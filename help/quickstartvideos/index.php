	<div>
		<div id="quickstart-1" class="faq-title">1. Add New Users to Your Organization and Group<a href="https://player.vimeo.com/video/363426205?badge=0&autopause=0&player_id=0&byline=0portrait=0owner=0" class="quickstart-guide">Watch Video</a></div>
		<p>
		As an Organization or a Group Admin, you are in charge of adding new members to your group. After talking to a prospective new member about the Coaching Companion, inform them that you will be creating a User Account for them. They will receive an automatic email with registration instructions when the account has been created.
		</p>
		<p>To create a new User Account:</p>
		<ul>
			<li>When logged into the Coaching Companion, navigate to <strong>Manage Users</strong>.</li>
			<li>On the <strong>Manage Users</strong> page, select the <strong>Add Users</strong> tab.</li>
			<li>Fill in the required information, paying attention to the Organization, Group and Role you are assigning.</li>
			<li>Be sure that the email address you use for the new user is the same email address that they will use to register with the ECLKC.</li>
			<li>Click <strong>Create User</strong></li>
			<li>The new user will receive an automatic email welcoming them to the Coaching Companion. In the email are instructions for registering with the ECLKC so they can log in. They must register using the same email that you used when creating the new user account, so that when they log in, they will already be in the correct Organization and Group.</li>
			<li>Even before the new user registers with the ECLKC, you can begin to include them in Cycles.</li>
			<li>If a person registers with the ECLKC and visits the Coaching Companion site before a User Account has been created for them, they will be put in the "New Users" organization. If this happens, you will receive a red error notification when trying to use their email to create a new user. In this case, do the following:</li>
			<OL type="a">
			<li>Navigate to <strong>Manage Users</strong>.</li>
			<li>In the search bar, enter the email of the user you want to add and click Search.</li>
			<li>If that user is already in the system and is in the "New Users" organization, they will appear at the bottom of the page.</li>
			<li>Click on the pencil icon in the table row of that user's email to edit the userâ€™s information.</li>
			<li>Change the Organization and Group of that user and click <strong>Update</strong>. The user is now in your organization.</li>
				</OL>
		</ul>
		</div>

		<div>
			<div id="quickstart-2" class="faq-title">
				2. Add New Groups to Your Organization<a href="https://player.vimeo.com/video/363427152?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</div>
			<p>
				As an Organization Admin, you are in charge of adding new Groups to your Organization. By creating a Group, placing one of the users in your Organization in the Group and making them a Group Admin, that user can invite their own new Coaches and Coachees, and then include these new users in Cycles.
			</p>
			<p>If a Coach is working alone and inviting people to become new Coachees, they should become Group Admin of their own Group.</p>
			<p>If a Coach is managing other Coaches and Coachees, they should become Group Admin of their own Group.</p>
			<p>You can appoint multiple people within a Group to be co-Admins of that group.</p>
			<p>To Add New Groups to Your Organization:</p>
			<ul>
				<li>When logged into the Coaching Companion, navigate to <strong>Manage Organizations/Groups</strong>.</li>
				<li>Enter a descriptive name for your new Group.</li>
				<li>Click <strong>Create</strong></li>
				<li>The page will reload and you will see your new Group in the list.</li>
			</ul>
		</div>

		<div>
			<div id="quickstart-3" class="faq-title">
				3. Change a User&apos;s Group<a href="https://player.vimeo.com/video/363428129?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</div>
			<p>
				As an Organization Admin, you are in charge of putting users in your organization into groups. If your organization does not require multiple groups, everyone in the organization can stay in the default group.
			</p>
			<p>If you are managing many users, you can organize them into various groups. The users in one group can only interact with other members of that same group.</p>
			<p>To Change a User&apos;s Group:</p>
			<ul>
				<li>When logged into the Coaching Companion, navigate to <strong>Manage Users</strong>.</li>
				<li>On the <strong>Manage Users</strong> page, locate the user you want to update. Alternatively, use the search bar and enter the user's name or email.</li>
				<li>Click the pencil icon in the same row as the user's name to edit the userâ€™s information.</li>
				<li>The <strong>Edit User</strong> tab will appear. Locate the "Group" field.</li>
				<li>Select the Group to which you would like to assign this user.</li>
				<li>Click <strong>Update User</strong></li>
			</ul>
		</div>

		<div>
			<p id="quickstart-4" class="faq-title">
				4. Create a New Practice-Based Coaching Cycle<a href="https://player.vimeo.com/video/363428474?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</p>
			<p>
			Coaches, Group Admins and Organization Admins can work with Coachees by creating a Practice-Based Coaching Cycle. This is at the core of the Coaching Companion.
			</p>
			<p>To Create a New Practice-Based Coaching Cycle:</p>
			<ul>
				<li>When logged into the Coaching Companion, navigate to <strong>PBC Cycles</strong>. This is your homepage; the first page you will see when you log in.</li>
				<li>From the <strong>PBC Cycles</strong> page, select the <strong>Add Cycle</strong> tab.</li>
				<li>Fill in each field and click <strong>Create Cycle</strong></li>
			</ul>
		</div>

		<div>
			<div id="quickstart-5" class="faq-title">
				5. Define or Edit a Goal for a Practice-Based Coaching Cycle<a href="https://player.vimeo.com/video/363428785?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</div>
			<p>
				Once a new PBC Cycle has been created, you will want to define a goal for the cycle.
			</p>
			<p>To add or edit a goal for a PBC Cycle:</p>
			<ul>
				<li>When logged into the Coaching Companion, navigate to <strong>PBC Cycles</strong>. Click on the name of the cycle you created.</li>
				<li>If a goal has not yet been created for the cycle, you will be brought to the <strong>Action Plan Goal</strong> page. If a goal has already been created, you will go straight to Action Plan Steps.</li>
				<li>On the <strong>Action Plan Goal</strong> page, fill in all fields and select <strong>Create</strong></li>
				<li>If you have already created a goal for the selected cycle and you wish to edit it, from the <strong>PBC Cycles</strong> page, select the cycle that has the goal you wish to edit. You should then find yourself on the <strong>Action Plan Steps</strong> page, expand the orange box at the top by clicking <strong>More</strong>. Select the <strong>Edit</strong> button associated with the goal.</li>
				<li>Make any edits you would like and select <strong>Update</strong> to save.</li>
			</ul>
		</div>

		<div>
			<div id="quickstart-6" class="faq-title">
				6. Define or Edit an Action Plan Step for a Practice-Based Coaching Cycle<a href="https://player.vimeo.com/video/363431355?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</div>
			<p>
			Once a new PBC Cycle has been created and a goal has been added to the cycle, you will want to create one or more Action Plan Steps.
			</p>
			To add or edit an Action Plan Step for a PBC Cycle:
			<ul>
				<li>When logged into the Coaching Companion, navigate to <strong>PBC Cycles</strong>. Click on the name of the cycle you created.</li>
				<li>If a goal has already been added to the selected cycle, you will be taken to the Action Plan Steps page.</li>
				<li>Fill out all the appropriate form fields and select <strong>Create</strong></li>
				<li>If the page does not reload, look at all the fields to make sure none are red, which would indicate that it has not been filled in.</li>
				<li>To edit, from the <strong>View Steps</strong> tab, select the <strong>Edit</strong> button next to the name of the step you wish to edit.</li>
				<li>Select the Action Plan Step you would like to edit.</li>
				<li>Make any edits you would like.</li>
				<li>Make your changes and select <strong>Update</strong> to save.</li>
			</ul>
		</div>

		<div>
			<div id="quickstart-7" class="faq-title">
				7. Define or Edit a Focused Observation for a Practice-Based Coaching Cycle<a href="https://player.vimeo.com/video/363431842?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</div>
			<p>
				Once a cycle has an Action Plan Step, you can create a Focused Observation to offer feedback about an Action Plan Step.	An Action Plan Step contains instructions for a Coachee, and a Focused Observation defines the scope of feedback that the Coach or a group of Observers will offer. There may be more than one specific scope of feedback, each defined in their own Focused Observation.
			</p>
			<p>To define or edit a Focused Observation for a PBC Cycle:</p>
			<ul>
				<li>When logged into the Coaching Companion, navigate to <strong>PBC Cycles</strong>. Click on the name of the cycle you just created.</li>
				<li>If a goal has already been added to the selected cycle, you will be taken to the Action Plan Steps page.</li>
				<li>Click the link that says <strong>Go to focused observations</strong> located next to the step you wish to add a Focused Observation to.</li>
				<li>Select the <strong>Add Observation</strong> tab.</li>
				<li>Add a descriptive title.</li>
				<li>Add the observer and the observed participants. Most commonly, you will be a Coach, selecting yourself as the observer, and selecting the Coachee you are working with as the observed participant, although you may want other users to participate.</li>
				<li>Add a Focus, describing the scope of feedback.</li>
				<li>Select the observation date</li>
				<li>Add preparation notes for the feedback.</li>
				<li>Select <strong>Create</strong></li>
				<li>If the page does not reload, look at all the fields to make sure none are red, which would indicate that it has not been filled in.</li>
				<li>To edit, select the <strong>View Observations</strong> tab and choose the <strong>Edit</strong> button next to the Focused Observation you wish to edit.</li>
				<li>Make any edits you would like and select <strong>Update</strong> to save.</li>
				<li>Each Focused Observation is directly associated with an Action Plan Step. This association cannot be edited. If the association is incorrect, delete this Focused Observation and created a new one.</li>
			</ul>
		</div>

		<div>
			<div id="quickstart-8" class="faq-title">
				8. Add Comments and Resources to an Action Plan Step or Focused Observation<a href="https://player.vimeo.com/video/363585546?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" class="quickstart-guide">Watch Video</a>
			</div>
			<p>
				Videos and other media (including Word Documents, Excel Documents, Images, and PDF's) can be uploaded and attached to Action Plan Steps as well as Focused Observations within Coaching Companion. A Coach may want to include an example video in an Action Plan Step so that a Coachee will have a clear idea what sort of video they should produce themselves, they can do this by adding a resource from the Resource Library.
			</p>
			<ul>
				<li>To add videos and other resources, from the <strong>View Steps</strong> tab or the <strong>View Observations</strong> tab, select <strong>Resources & Comments</strong> under the name of the step/observation you wish to add resources to.</li>
				<li>To upload a video, select <strong>Add New Resource</strong> and follow the prompts.</li>
				<li>The resource you upload will appear at the bottom. You may need to refresh the page if it doesnâ€™t automatically appear.</li>
				<li>To add a resource from the Resource Library, select <strong>Add Resource from Resource Library</strong>.</li>
				<li>Select the circle icon with the plus sign inside, located next to the desired resource. This will open a menu with a button to add the resource to the selected step/observation. Alternatively, you can view the resource first, by clicking the title of the resource and from that page you will also see a button to add the resource to the chosen step/observation.</li>
				<li>The resource will appear at the bottom of the page.</li>
				<li>To add a time-stamped comment on a video, click directly on the video while it is playing and a comment field will appear.</li>
				<li>To make comments which are not directly linked to a video, select <strong>Add Comment to Step</strong> (or Observation if you are on the Focused Observation page).</li>
				<li>To reply to an existing comment, click on <strong>Reply</strong> under the comment.</li>
				<li>You have the options to <strong>Edit</strong> and <strong>Delete</strong> comments that you have made.</li>
			</ul>
