<div id="quickstart-1" class="faq-title">Add New Users to Your Organization and Group&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253831886?badge=0&amp;autopause=0&amp;player_id=0&amp;byline=0&amp;portrait=0&amp;owner=0" >Watch Video</a></div>
<p>
As an Organization or a Group Admin, you are in charge of adding new members to your group.
After talking to a prospective new member about the Coaching Companion, inform them that you will be creating a User Account for them. They will receive an automatic email with registration instructions when the account has been created.</p>
To create a new User Account:<br>
<ul>
	<li>	When logged into the coaching companion, navigate to CCAdmin.</li>
	<li>From the CCAdmin page, navigate to Manage Users.</li>
	<li>On the Manage Users page, open the Create a New User bar.</li>
	<li>Fill in the required information, paying attention to the Organization, Group and Role you are assigning.</li>
	<li>Be sure that the email address you use for the new User is the same email address that they will use to register with the ECLKC.</li>
	<li>Click "Create User"</li>
	<li>The new user will receive an automatic email welcoming them to the Coaching Companion. In the email are instructions for registering with the ECLKC so they can log in. They must register using the same email that you used when creating the new user account, so that when they log in, they will already be in the correct Organization and Group.</li>
	<li>Even before the new user registers with the ECLKC, you can begin to include them in Cycles.</li>
	<li>If a person registers with the ECLKC and visits the Coaching Companion site before a User Account has been created for them, they will be put in the New Users organization. If this happens, you will receive a red error notification when trying to use their email to create a New User. In this case, do the following:</li>
	<OL type="a">
	<li>Navigate to Manage Users.</li>
	<li>Open the Search for Users bar.</li>
	<li>Enter the email of the user you want to add and click Search.</li>
	<li>If that user is already in the system and is in the New Users organization, they will appear at the bottom of the page.</li>
	<li>Click on Edit in the table row of that user's email.</li>
	<li>Edit the Organization and Group of that user and click Update.  The user is now in your organization.</li>
    </OL>
</ul>
</p>

<div id="quickstart-2" class="faq-title">Add New Groups to Your Organization&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253918565?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
As an Organization Admin, you are in charge of adding new Groups to your Organization.<br>
By creating a Group, placing one of the users in your Organization in the Group and making them a Group Admin, that user can invite their own new Coaches and Coachees, and then include these new users in Cycles.<br>
If a Coach is working alone and inviting people to become new Coachees, they should become Group Admin of their own Group.<br>
If a Coach is managing other Coaches and Coachees, they should become Group Admin of their own Group.<br>
You can appoint multiple people within a Group to be co-Admins of that group.
</p>
To Add New Groups to Your Organization:<br>
<ul>
	<li>When logged into the coaching companion, navigate to CCAdmin.</li>
	<li>From the CCAdmin page, navigate to Manage Organizations.</li>
	<li>On the Manage Organizations page, open the Create a New Group bar.</li>
	<li>Enter a descriptive name for your new Group.</li>
	<li>Click "Add"</li>
	<li>The page will reload and you will see your new Group in the list.</li>
</ul>
</p>

<div id="quickstart-3" class="faq-title">Change a User&apos;s Group&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253918685?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
As an Organization Admin, you are in charge putting users in your Organization into Groups.<br>
If your Organization does not require multiple groups, everyone in the Organization can stay in the Default Group.<br>
If you are managing many users, you can organize them into various Groups. The users in one group can only interact with other members of that same Group.<br>
</p>
To Change a User&apos;s Group:<br>
<ul>
	<li>When logged into the coaching companion, navigate to CCAdmin.</li>
	<li>From the CCAdmin page, navigate to Manage Users.</li>
	<li>On the Manage Users page, locate the user you want to update. Alternately, click on the Search for Users bar and enter the user&apos;s name or email.</li>
	<li>Click &quot;Edit&quot; in the same row as the user&apos;s name</li>
	<li>The Update User page will load. Locate the &quot;Group&quot; field.</li>
	<li>Select the Group you to which you would like to assign this user.</li>
	<li>Click &quot;Update User&quot;</li>
</ul>
</p>

<div id="quickstart-4" class="faq-title">Create a New Practice Based Coaching Cycle&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253918769?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
As a Coach, Group Admin or Organization Admin, you can work with Coachees by creating a Practice Based Coaching Cycle. This is at the core of the Coaching Companion.<br>
</p>
To Create a New Practice Based Coaching Cycle:<br>
<ul>
	<li>When logged into the coaching companion, navigate to PBC Cycles. This is your home page; the first page you will see when you log in.</li>
	<li>From the PBC Cycles page, open the  &quot;Add New PBC Cycle&quot; bar.</li>
	<li>Give your new Cycle a descriptive name.</li>
	<li>Choose the date when the Cycle will begin.</li>
	<li>Choose the date when the Cycle will be reviewed.</li>
	<li>Click &quot;Update User&quot;</li>
</ul>
</p>

<div id="quickstart-5" class="faq-title">Define or Edit a Goal for a Practice Based Coaching Cycle&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253918927?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
As a Coach, Group Admin or Organization Admin who has created a new Cycle, you will want to define a Goal for the Cycle.<br>
</p>
To Define or Edit a Goal for a Practice Based Coaching Cycle:<br>
<ul>
	<li>When logged into the coaching companion, navigate to PBC Cycles. Click on the name of the Cycle you just created.</li>
	<li>From the Cycle page, open the  &quot;Add PBC Cycle Steps&quot; bar.</li>
	<li>Open the  &quot;Add Action Plan Goal&quot; bar.</li>
	<li>Fill in this cycle's goal over &quot;What is your goal for this coaching cycle?&quot; </li>
	<li>Fill how you will know how this goal will be reached over &quot;How will you know when you have achieved this goal?&quot; </li>
	<li>Click &quot;Create&quot;</li>
	<li>To edit, open &quot;Action Plan Goal&quot; within the window &quot;View PBC Cycle Steps&quot;</li>
	<li>Make any edits you would like.</li>
	<li>When you want to save your edits, click &quot;Edit&quot;</li>
</ul>
</p>

<div id="quickstart-6" class="faq-title">Define or Edit an Action Plan Step for a Practice Based Coaching Cycle&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253919337?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
As a Coach, Group Admin or Organization Admin who has created a new Cycle, you will want to create one or more Action Plan Steps.<br>
</p>
To Define or Edit an Action Plan Step for a Practice Based Coaching Cycle:<br>
<ul>
	<li>When logged into the coaching companion, navigate to PBC Cycles. Click on the name of the Cycle you just created.</li>
	<li>From the Cycle page, open the  &quot;Add PBC Cycle Steps&quot; bar.</li>
	<li>Open the  &quot;Add an Action Plan Step&quot; bar.</li>
	<li>Add an Action Plan Step Name.</li>
	<li>Add a Start Date and an End Date for the Action Plan Step.</li>
	<li>Open the Action Plan Step Assignee Bar.</li>
	<li>You will see all of the users who are in the same Group as you. Drag the name of all users who are participating in this step from the list into field below. Most commonly, you will be a Coach, selecting the name of a single Coachee.</li>
	<li>Add comments.</li>
	<li>Click &quot;Create&quot;</li>
	<li>If the page does not reload, look at all the fields to make sure none are red, which would indicate that it has not been filled in.</li>
	<li>To edit, open &quot;View Action Plan Steps&quot; within the window &quot;View PBC Cycle Steps&quot;</li>
	<li>Select the Action Plan Step you would like to edit.</li>
	<li>Make any edits you would like.</li>
	<li>This is also the place to add videos and other media. You can upload your own video or choose pre-existing videos from the Resource Library.</li>
	<li>To upload a video, open the &quot;Add New Media&quot; bar and follow instructions.</li>
	<li>The media will appear at the bottom of the page.</li>
	<li>To use supplied media from the resource library, open the &quot;Associated Resources&quot; bar.</li>
	<li>Click on the &quot;Add a resource from the Resource Library&quot;.</li>
	<li>Locate the resource you are searching for and click on &quot;View&quot;</li>
	<li>Scroll down and click on &quot;Associate this resource...&quot;.</li>
	<li>The media will appear at the bottom of the page.</li>
	<li>When you want to save your edits, click &quot;Update&quot;</li>
</ul>
</p>

<div id="quickstart-7" class="faq-title">Define or Edit a Focused Observation for a Practice Based Coaching Cycle&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253919635?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
As a Coach, Group Admin or Organization Admin who has created a new Cycle and an Action Plan Step, you will want to create a Focused Observation to offer feedback about an Action Plan Step.<br>
An Action Plan Step contains instructions for a Coachee, and a Focused Observation defines the scope of feedback that the Coach or a group of Observers will offer. There may be more than one specific scopes of feedback, each defined in their own Focused Observation.<br>
When a Focused Observation is created, an accompanying Reflection and Feedback is created automatically, which is where real discussion takes place.
</p>
To Define or Edit a Focused Observation for a Practice Based Coaching Cycle:<br>
<ul>
	<li>When logged into the coaching companion, navigate to PBC Cycles. Click on the name of the Cycle you created.</li>
	<li>From the Cycle page, open the  &quot;Add PBC Cycle Steps&quot; bar.</li>
	<li>Open the  &quot;Add a Focused Observation&quot; bar.</li>
	<li>Select an Action Plan Step that you are observing.</li>
	<li>Open the  &quot;Observers&quot; bar.</li>
	<li>You will see all of the users who are in the same Group as you. Drag the name of all users who are participating in this Focused Observation from the list into field below. Most commonly, you will be a Coach, selecting yourself as the Observer, although you may want other users to participate.</li>
	<li>Open the  &quot;Observed Participants&quot; bar.</li>
	<li>You will see all of the users who are in the same Group as you. Drag the name of all users who are being observed in this Focused Observation from the list into field below. Most commonly, you will be a Coach, selecting the Coachee you are working with in this cycle, although you may want other users to participate.</li>
	<li>Add a Focus, describing the scope of feedback.</li>
	<li>Select a date.</li>
	<li>Add preparation notes for the feedback.</li>
	<li>Click &quot;Create&quot;</li>
	<li>If the page does not reload, look at all the fields to make sure none are red, which would indicate that it has not been filled in.</li>
	<li>To edit, open &quot;View Focused Observations&quot; within the window &quot;View PBC Cycle Steps&quot;</li>
	<li>Select the Focused Observation you would like to edit.</li>
	<li>Make any edits you would like.</li>
	<li>Each Focused Observation is directly associated with an Action Plan Step. This association cannot be edited. If the association is incorrect, delete this Focused Observation and created a new one.</li>
	<li>When you want to save your edits, click &quot;Update&quot;</li>
</ul>
</p>

<div id="quickstart-8" class="faq-title">Communicate with all Cycle Participants in the Reflection and Feedback Step&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253920099?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
If you have been included in a Focused Observation, you will want to communicate with the other participants in a Cycle about the focus on an Action Plan Step.<br>
When a Focused Observation is created, an accompanying Reflection and Feedback is created automatically, which is where real discussion takes place.
</p>
To Communicate with all Cycle Participants in the Reflection and Feedback Step:<br>
<ul>
	<li>When logged into the coaching companion, navigate to PBC Cycles. Click on the name of the Cycle you created.</li>
	<li>From the Cycle page, look in the  &quot;View PBC Cycle Steps&quot; window.</li>
	<li>Open the  &quot;View Reflection and Feedback Responses&quot; bar.</li>
	<li>Select the feedback you want to participate in.</li>
	<li>You will see the associated media on this page, an &quot;Add Associated Resources&quot; bar and an &quot;Add Comments&quot; bar.</li>
	<li>All of the comments being made during this feedback will appear in comment trees.</li>
	<li>To comment on any videos on the page, click directly on the video while it is playing and a comment field will appear.</li>
	<li>To make comments which are not directly linked to a video, open the &quot;Add Comments&quot; bar.</li>
	<li>To reply to an existing comment, click on &quot;Reply&quot; under the comment in question.</li>
	<li>You have the options to &quot;Edit&quot; and &quot;Delete&quot; comments that you have made.</li>
</ul>
</p>

<div id="quickstart-9" class="faq-title">Upload a Video or Other Media for an Action Plan Step&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253920500?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
Videos and other media (including Word Documents, Excel Documents, Images, and PDF's) can be uploaded on various pages within the Coaching companion<br>
Here we will look at the Cycle Goal, where a Needs Assessment document can be uploaded if wanted.
</p>
To Upload a Video or Other Media for an Action Plan Step:<br>
<ul>
	<li>When logged into the coaching companion, navigate to PBC Cycles. Click on the name of the Cycle you created.</li>
	<li>From the Cycle page, look in the  &quot;View PBC Cycle Steps&quot; window.</li>
	<li>Navigate to the edit page of the Action Plan Goal</li>
	<li>Scroll down and open the &quot;Add New Media&quot; bar.</li>
	<li>Follow the instructions to upload new media or choose from media you have uploaded in the past.</li>
	<li>The page will reload, and the media you uploaded will appear at the bottom.</li>
</ul>
</p>

<div id="quickstart-10" class="faq-title">Use the Resource Library&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://player.vimeo.com/video/253920882?badge=0&autopause=0&player_id=0&byline=0&portrait=0&owner=0" >Watch Video</a></div>
<p>
Videos and other media (including Word Documents, Excel Documents, Images, and PDF's) which are to be used as examples have been uploaded to the Resource Library.<br>
These are available to peruse, and can be associated to various steps within a Cycle. Specifically, a Coach may want to include an example video in an Action Plan Step so that a Coachee will have a clear idea what sort of video they should produce themselves.
</p>
To Use the Resource Library<br>
<ul>
