Some coaches in the OHS Coaching Companion will be invited to work with more than one organization. This is an agreement between the Organization Admin and the prospective coach. <br>
The way this would normally happen is that a personal communication between the Organization Admin and the Coach would happen, then the Coach requests to become part of the new organization, following the steps below. <br>
To become a Multi-Organizational Coach:<br>
<ul>
	<li>Begin by communicating with the Organization Admin about coaching in their organization.</li>
	<li>Once you have agreed to coach in the new organization, log in to the OHS Coaching Companion and navigate to &quot;My Account&quot; page.</li>
	<li>At the bottom of the My Account page, click on the &quot;Request Access&quot; button.</li>
	<li>Select the organization you have been invited to coach in.</li>
	<li>Write a short note to the Organization Admin which will be sent with your request.</li>
	<li>Click on &quot;Send Request&quot;.</li>
	<li>The Organization Admins of the organization you chose will receive and email with your request.</li>
	<li>In the email is a link which will bring them to the Request Access page, with a form to accept the coach&apos;s request.</li>
	<li>When the Organization Admin accepts the coach&apos;s request, the coach will receive an email to that effect.</li>
	<li>After their request has been granted, the coach will see a list of the organizations they belong to in their My Account page.</li>
	<li>A coach can only work in one organization at a time, so they will switch between organizations by selecting the one they wish to work with and clicking &quot;Update&quot; in their My Account page.</li>
</ul>
